public class While {
    public static void main(String[] args){

        int sum = 0;
        int no = 1;

       while(no<=20) {
            
           sum = sum + no ;
           no=no+1 ;
            }
       System.out.println("sum of first 20 numbers = "+sum ) ;

    }

}
